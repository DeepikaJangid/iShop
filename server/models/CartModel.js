const mongoose = require('mongoose');

const CartSchema = new mongoose.Schema(
    {
        user_id: {
            type: mongoose.Schema.Types.ObjectId,
            ref: "User",
            required: true,
        },
        product_id: {
            type: mongoose.Schema.Types.ObjectId,
            ref: "Product",
            required: true,
        },
        quantity: {
            type: Number,
            required: true,
            default: 1,
            min: 1,
        },
    },
    {
        timestamps: true,
    }
);

const CartModel = mongoose.model('Cart', CartSchema);
module.exports = CartModel;